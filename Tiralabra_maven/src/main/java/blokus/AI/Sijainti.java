package blokus.AI;

/**
 * Yksinkertainen koordinaatti sijainti luokka
 * @author Ilkimys
 */
public class Sijainti {
    
    int i;
    int j;

    /**
     *
     * @param i
     * @param j
     */
    public Sijainti(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }
    
    
    
    
}
