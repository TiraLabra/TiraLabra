package viidensuora.ai;

public class Siirto {

    private final int arvo;
    private final int i;
    private final int j;

    public Siirto(int arvo, int i, int j) {
        this.arvo = arvo;
        this.i = i;
        this.j = j;
    }

    public int getArvo() {
        return arvo;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

}
