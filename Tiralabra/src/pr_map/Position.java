
package pr_map;

/**
 *
 * @author Henri Korpela
 * Describes position in with integers x and y.
 */
public class Position {
    /**
     * X coordinate.
     */
    public int x;
    /**
     * Y coordinate.
     */
    public int y;
    /**
     * Creates position.
     * @param x X coordinate.
     * @param y Y coordinate.
     */
    public Position(int x,int y)
    {
        this.x = x;
        this.y = y;
    }
}
